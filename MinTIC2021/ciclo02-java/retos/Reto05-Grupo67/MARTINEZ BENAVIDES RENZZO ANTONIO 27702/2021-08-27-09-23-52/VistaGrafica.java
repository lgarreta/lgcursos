    public void mostrarEstudiante(ArrayList<Estudiante> listaEstudiante) {
        for (int i = 0; i < listaEstudiante.size(); i++) {
            Estudiante e = listaEstudiante.get(i);
            AreaEstudiantes.append(e.nombres+"\n");
            AreaEstudiantes.append(e.apellidos+"\n");
            AreaEstudiantes.append(e.fechaNac+"\n");
            AreaEstudiantes.append(e.correoInstitucional+"\n");
            AreaEstudiantes.append(e.correoPersonal+"\n");
            AreaEstudiantes.append(e.numeroCelular+"\n");
            AreaEstudiantes.append(e.numeroFijo+"\n");
            AreaEstudiantes.append(e.progAcademico+"\n");
            
           
           }
    }


