    private void BotonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAdicionarActionPerformed
        String nombres = textNombres.getText();
        String apellidos = texApellidos.getText();
        String nacimiento = texNacimiento.getText();
        String correoInst = TextCorreoInst.getText();
        String correoPers = texCorreoPers.getText();
        Long nroCelular = new Long (TextNroCelular.getText());
        Long nroFijo = new Long (texNroFijo.getText());
        String academico = texAcademico.getText();
        Estudiante e = new Estudiante(nombres,apellidos,nacimiento,correoInst,correoPers,nroFijo,nroCelular, academico);
        controlador.adicionar(e);
        textNombres.setText("");
        texApellidos.setText("");
        texNacimiento.setText("");
        TextCorreoInst.setText("");
        texCorreoPers.setText("");
        TextNroCelular.setText("");
        texNroFijo.setText("");
        texAcademico.setText("");
    }//GEN-LAST:event_BotonAdicionarActionPerformed

